#include "3-calc.h"

/**
 * op_add - adds two numbers together
 * a: the first number
 * b: the second number
 * return: the result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two numbers together
 * a: the first number
 * b: the second number
 * return: the result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers together
 * a: the first number
 * b: the second number
 * return: the result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divises two numbers together
 * a: the first number
 * b: the second number
 * return: the result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modules two numbers together
 * a: the first number
 * b: the second number
 * return: the result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
